const addTwoNumbers = (num1, num2) => {
  // add two numbers & return the result
  return num1 + num2;
};

console.log(addTwoNumbers(1, 2));
console.log(addTwoNumbers(10, 20));
console.log(addTwoNumbers(100, 200));
console.log(addTwoNumbers(10, -20));
console.log(addTwoNumbers(-100, -200));
console.log(addTwoNumbers(0, 0));
